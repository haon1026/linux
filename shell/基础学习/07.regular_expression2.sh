#!/bin/bash

#正则表达式

# "\{n\}"表示其前面的字符恰好出现n次
grep "a\{3\}" rule.txt
#匹配a字母连续出现三次的字符串

grep "[0-9]\{3\}" rule.txt
#匹配包含连续的三个数字的字符串
echo -e "\n--------------------\n"

# "\{n,\}"表示其前面的字符出现不小于n次
grep "^[0-9]\{3,\}" rule.txt
#匹配最少用连续三个数字开头的行
echo -e "\n--------------------\n"

# "\{n,m\}"匹配其前面的字符至少出现n次，最多出现m次
grep "sa\{1,3\}i" rule.txt
#匹配在字母s和字母i之间有最少一个a，最多三个a
